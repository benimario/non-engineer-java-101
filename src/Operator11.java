public class Operator11 {
    public static void main(String[] args) {
        System.out.println(2 < 3);
        System.out.println(2 <= 3);
        System.out.println(2 > 3);
        System.out.println(3 >= 3);
        System.out.println(5 == 3);
        System.out.println(5 == 5);
        System.out.println(5 != 5);
        System.out.println(3 != 5);
    }
}




